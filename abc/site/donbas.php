            Донбасская операция.</p>

        <h2>Итоги</h2>
        <p>В результате операции был полностью освобождён Донецкий бассейн.</p>
        <p>В связи с отступлением на Левобережной Украине немецкая армия была вынуждена оставить Кубань, эвакуировав
            17-ю армию в Крым, где она находилась, не принимая участия в осенне-зимней кампании 1943-44 годов, до
            апреля-мая 1944 года, когда была полностью уничтожена. В ходе операции советские войска продвинулись на 300
            км и вышли на линию Днепропетровск-Мелитополь. Потеря Донецкого угольного бассейна нанесла большой удар
            экономике Германии, Советский Союз, напротив, получил в 1944 году 21.1 млн тонн угля. К началу 1945 года
            добыча угля была налажена на 3/4 шахт. Уже в 1943 г. на Енакиевском металлургическом заводе был восстановлен
            полный металлургический цикл, всего через 30 дней после освобождения дали ток генераторы Зуевской ТЭЦ и
            Рудченской ГЭС. К сентябрю 1944 года выпуск продукции машиностроения юга Украины достиг 30% довоенного
            уровня[13]. Сельское хозяйство вернуло миллионы гектаров посевных площадей на Украине и Кубани.</p>
        <h2>Послевоенная оценка</h2>
        <p>В 1948 году английским трибуналом за преступления на Украине Манштейн был осужден[14] к 18 годам тюремного
            заключения. Однако уже в 1953 году он вышел на свободу.</p>
