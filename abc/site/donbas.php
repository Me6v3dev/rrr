        <h2>Донбасская операция (1943)</h2>
        <p>Донбасская операция (13 августа — 22 сентября 1943) — стратегическая военная операция Вооружённых сил СССР
            против войск нацистской Германии с целью освобождения Донецкого бассейна. Проводилась войсками Юго-Западного
            фронта под командованием генерала армии Р.Я. Малиновского и Южного фронта под командованием
            генерал-полковника Ф.И. Толбухина при поддержке Азовской флотилии. Итогами операции стали продвижение
            советских войск на расстояние до 300 км, завершение освобождения Донбасса (в том числе городов Донецк,
            Таганрог, Мариуполь) и выход к Днепру. Были разгромлены 13 дивизий из состава немецкой группы армий “Юг”
            [3].</p>

        <h2>Предыстория</h2>
        <p>В июле 1943 года советские войска провели Изюм-Барвенковскую и Миусскую наступательные операции. Немецкое
            командование перебросило значительные силы на ликвидацию прорывов, образовавшихся в результате этих
            наступлений, что помогло советским войскам во время Курской битвы сначала сдержать натиск немецких сил, а
            потом нанести им мощный контрудар.</p>
        <p>После этого советские войска начали Белгородско-Харьковскую стратегическую наступательную операцию
            «Румянцев». В начале августа 1943 года немецкое командование прилагало все усилия, чтобы остановить
            наступление советских войск на харьковском направлении, перебросив туда с Донбасского плацдарма 15 дивизий,
            в том числе 4 танковые[4].</p>

        <h2>Ход операции</h2>
        <p>Донбасская операция началась 13 августа 1943 наступлением правого крыла Юго-Западного фронта. Эти войска
            форсировали Северский Донец и продвигаясь вдоль правого берега реки, помогали Степному фронту в освобождении
            Харькова. Начатое 16 августа наступление в центре фронта развития не получило. Советские войска были
            остановлены на реке Миус, где немцы построили сильно укреплённый оборонительный рубеж. Тем не менее, это
            наступление заставило немцев использовать для его отражения значительную часть своих резервов.</p>
        <p>8 августа, воспользовавшись ситуацией, в наступление перешли войска Южного фронта. В первый же день
            наступления советские войска прорвали оборону на глубину 8–9 километров, а к концу дня 20 августа прорыв
            составлял уже до 24 километров в глубину и до 16 в ширину. В ночь на 24 августа советские войска перерезали
            дорогу на Таганрог, лишив противника возможности маневрировать резервами. 28 августа началось сражение за
            Саур-Могилу, которое продолжалось трое суток. Утром 31 августа бойцы 96-й гвардейской стрелковой дивизии
            окончательно сбили противника с вершины этого холма[5].</p>
        <p>30 августа при содействии морского десанта был освобождён Таганрог. В районе города был окружён и уничтожен
            29-й немецкий корпус[6]. После того как над группой армий «Юг» нависла угроза расчленения и уничтожения,
            Гитлер разрешил её частям отойти за Днепр.</p>
        <p>1 сентября немецкие войска начали отступать по всему фронту в Донбассе. 3 сентября 1943 года советские войска
            освободили Калиновку, пригород Горловки. 5 сентября 1943 года город был полностью освобождён от немецкой
            оккупации, и одновременно ударные части Южного фронта освободили Артемовск[7], а 8 сентября — столицу
            Донбасса Сталино (ныне Донецк)[8]. 10 сентября при содействии морского десанта был освобождён Мариуполь.</p>
        <p>Отход немецких частей отличался исключительно тяжелыми потерями вермахта. Генерал-фельдмаршал Эрих фон
            Манштейн отмечал:</p>
