            промежуточных оборонительных рубежей. Также препятствием к проведению планомерного отхода являлись действия
            советских партизан, наносивших многочисленные удары по железным дорогам и мостам в тылу немецкой армии на
            Правобережной Украине, сводя пропускную способность коммуникаций к минимуму.</p>
        <p>При отступлении немецкая армия использовала тактику выжженной земли. Основной целью создания «зон пустыни»
            было снизить темпы советского наступления и выиграть время для создания мощного оборонительного рубежа на.
            Днепр, сохранив при этом крупные плацдармы на Левом берегу Днепра. Несмотря на то, что отступающими
